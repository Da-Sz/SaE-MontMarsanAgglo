function getColor(d) {
  return d > 25000
    ? "#800026"
    : d > 15000
    ? "#BD0026"
    : d > 10000
    ? "#E31A1C"
    : d > 5000
    ? "#FC4E2A"
    : d > 1000
    ? "#FD8D3C"
    : d > 500
    ? "#FEB24C"
    : d > 200
    ? "#FED976"
    : "#FFEDA0";
}

function style(feature) {
  return {
    fillColor: getColor(feature.properties.population),
    weight: 2,
    opacity: 1,
    color: "white",
    dashArray: "3",
    fillOpacity: 0.7,
  };
}

L.geoJson(statesData, { style: style }).addTo(map);

function highlightFeature(e) {
  var layer = e.target;

  layer.setStyle({
    weight: 5,
    color: "#666",
    dashArray: "",
    fillOpacity: 0.7,
  });

  if (!L.Browser.ie && !L.Browser.opera && !L.Browser.edge) {
    layer.bringToFront();
  }
  info.update(layer.feature.properties);
}

function resetHighlight(e) {
  geojson.resetStyle(e.target);
  info.update();
}

